PDG.displayresponse={};

//////////////////////////////////////////////////////////////////////
/*PDG.displayresponse.init 
*/
//////////////////////////////////////////////////////////////////////
PDG.displayresponse.init= function()
{
	//we need the success responder...
	var vResponse=PDG.findResponder(true);
	if(PDG.isObject(vResponse))
		vResponse(window.document,document.location,pdg_modname);
}
//////////////////////////////////////////////////////////////////////
/*we do not have to wait for the dom to load....
*/
//////////////////////////////////////////////////////////////////////
PDG.displayresponse.init();
