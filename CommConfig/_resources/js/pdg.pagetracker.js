//////////////////////////////////////////////////////////////////////	
/*pageTracker class def
*/
//////////////////////////////////////////////////////////////////////	
PDG.pageTracker=function()
{	
	this.pages= new Object;
}
//////////////////////////////////////////////////////////////////////	
/*pageTracker resetContents
	
	clears all pages....
	
*/
//////////////////////////////////////////////////////////////////////	
PDG.pageTracker.prototype.resetContents=function()
{
	this.pages= new Object;
}
//////////////////////////////////////////////////////////////////////	
/*pageTracker addPage
	adds a page and returns the reference
	if the page already exists it returns the existing reference
*/
//////////////////////////////////////////////////////////////////////	
PDG.pageTracker.prototype.addPage=function(pageid)
{
	if(!PDG.isObject(this.pages[pageid]))
		this.pages[pageid]=new PDG.pageInstance(pageid);
	
	return this.pages[pageid];
}
//////////////////////////////////////////////////////////////////////	
/*pageTracker hasPage

*/
//////////////////////////////////////////////////////////////////////	
PDG.pageTracker.prototype.hasPage=function(pageid)
{
	return PDG.isObject(this.pages[pageid]);
}
//////////////////////////////////////////////////////////////////////	
/*pageTracker removePage
	removes a page from the tracker...	
*/
//////////////////////////////////////////////////////////////////////	
PDG.pageTracker.prototype.removePage=function(pageid)
{
	if(!PDG.isObject(this.pages[pageid]))
		return;
	delete this.pages[pageid];
}
//////////////////////////////////////////////////////////////////////	
/*pageInstance class def
*/
//////////////////////////////////////////////////////////////////////	
PDG.pageInstance=function(id)
{
	this.id=id;
	this.sectionid='default';
}